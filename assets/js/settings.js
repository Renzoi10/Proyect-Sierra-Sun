$(document).ready(function(){
    // Activar información sobre herramientas
    $('[data-toggle="tooltip"]').tooltip();
    
    // Seleccionar / deseleccionar casillas de verificación
    var checkbox = $('table tbody input[type="checkbox"]');
    $("#selectAll").click(function(){
        if(this.checked){
            checkbox.each(function(){
                this.checked = true;                        
            });
        } else{
            checkbox.each(function(){
                this.checked = false;                        
            });
        } 
    });
    checkbox.click(function(){
        if(!this.checked){
            $("#selectAll").prop("checked", false);
        }
    });
});
//Fin de funciones para la mantenimiento de empleados



